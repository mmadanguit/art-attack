class MathOperations:
    def __init__(self, x, y):
        """Instantiate MathOperations object with two numbers to operate on.

        Parameters:
            x (int): first number.
            y (int): second number.
        """
        self.x = x
        self.y = y

    def add(self):
        """Add x and y together. X and y have been defined in the init.

        Returns:
            (int): x plus y.
        """
        return x+y
